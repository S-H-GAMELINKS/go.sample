package main

import "fmt"

func main() {
    sum := 1
    
    if sum >= 10 {
        fmt.Println(sum)
    }
    
    if sum > 10 {
        fmt.Println(sum)   
    }
    
    if sum <= 10 {
        fmt.Println(sum)   
    }
    
    if sum < 10 {
        fmt.Println(sum)   
    }
    
    if sum == 1 {
        fmt.Println(sum)   
    }
    
    if sum != 10 {
        fmt.Println(sum)   
    }
}
